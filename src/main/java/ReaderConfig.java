
public class ReaderConfig {

    public String protocolForRetrievingData;
    public String messageAccessProtocol;
    public String host;
    public String email;
    public String password;

    public ReaderConfig(String protocolForRetrievingData, String messageAccessProtocol, String host, String email, String password) {
        this.protocolForRetrievingData = protocolForRetrievingData;
        this.messageAccessProtocol = messageAccessProtocol;
        this.host = host;
        this.email = email;
        this.password = password;
    }








}
